def calculat_parallelogram_area(base,height):
    area=base*height
    return area
    
base_value_input=int(input("enter the base value : "))
height_value_input=float(input("enter the height value : "))

print ("calculat parallelogram area : ", calculat_parallelogram_area(base=base_value_input,height=height_value_input))
